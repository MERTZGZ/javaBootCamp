package conditionals;

public class Main {

	public static void main(String[] args) {
		int number = 30 ;
		
		if (number<20) {
			System.out.println("the number is less than twenty ");
		}
		
		else if (number<15) {
			System.out.println("the number is less than fifteen ");			
		}
		
		else if (number==20) {
			System.out.println("the number is equal to twenty ");
		}
		
		else if (number==15){
			System.out.println("the number is equal to twenty fifteen ");
		}
		
		else {
			System.out.println("the number is not less than twenty ");
			
		}
			
	}

}
